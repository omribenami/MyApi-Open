/**
 * Vault Token Instructions API Routes
 * Phase 4.5: API Instruction Layer for Token Vault
 * 
 * Enables external AI agents to discover and learn how to use any service
 * through discoverable API instructions.
 */

const express = require('express');
const { v4: uuidv4 } = require('uuid');

function createVaultInstructionsRoutes(db, tokenManager, auditLog) {
  const router = express.Router();

  // Helper functions
  const requireAuth = (req, res, next) => {
    if (!req.session?.user && !req.tokenMeta) {
      return res.status(401).json({ error: 'Unauthorized' });
    }
    next();
  };

  const resolveUserId = (req) => {
    return String(req.session?.user?.id || req.tokenMeta?.ownerId || req.tokenMeta?.userId || 'owner');
  };

  const logInstructionAction = (req, action, tokenId, status, details = null) => {
    if (auditLog) {
      auditLog.log({
        tokenId: tokenId,
        requester: resolveUserId(req),
        action,
        endpoint: req.path,
        method: req.method,
        status,
        details,
        ipAddress: req.ip,
        userAgent: req.get('user-agent')
      });
    }
  };

  /**
   * GET /api/v1/vault/tokens/:id
   * Get token metadata with instructions
   * Returns full token details including stored instructions
   */
  router.get('/tokens/:id', requireAuth, (req, res) => {
    try {
      const { id } = req.params;
      const userId = resolveUserId(req);

      // Get token from database
      const stmt = db.prepare(`
        SELECT * FROM access_tokens WHERE id = ? AND owner_id = ?
      `);
      const token = stmt.get(id, userId);

      if (!token) {
        logInstructionAction(req, 'vault:get_token', id, 404);
        return res.status(404).json({ error: 'Token not found' });
      }

      // Get instructions if they exist
      const instructionsStmt = db.prepare(`
        SELECT * FROM vault_token_instructions WHERE token_id = ?
      `);
      const instructions = instructionsStmt.get(id);

      const response = {
        id: token.id,
        name: token.name,
        type: token.type,
        scope: token.scope,
        createdAt: token.created_at,
        expiresAt: token.expires_at,
        revokedAt: token.revoked_at,
        instructions: instructions ? {
          id: instructions.id,
          instructions: instructions.instructions,
          examples: instructions.examples ? JSON.parse(instructions.examples) : [],
          autoGenerated: instructions.auto_generated === 1,
          learnedFromSkill: instructions.learned_from_skill_id,
          learnedFromAgent: instructions.learned_from_agent_id,
          learnedAt: instructions.learned_at,
          createdAt: instructions.created_at,
          updatedAt: instructions.updated_at
        } : null
      };

      logInstructionAction(req, 'vault:get_token', id, 200);
      res.json({ success: true, data: response });
    } catch (error) {
      logInstructionAction(req, 'vault:get_token', req.params.id, 500, { error: error.message });
      console.error('[VaultInstructions] Error getting token:', error);
      res.status(500).json({ error: 'Failed to get token' });
    }
  });

  /**
   * GET /api/v1/vault/tokens/:id/instructions
   * Get just the instructions for a token
   * Lightweight endpoint for AI agents to fetch instructions
   */
  router.get('/tokens/:id/instructions', (req, res) => {
    try {
      const { id } = req.params;

      // Check if token exists (no auth required for reading public instructions)
      const tokenStmt = db.prepare('SELECT id FROM access_tokens WHERE id = ?');
      const token = tokenStmt.get(id);

      if (!token) {
        logInstructionAction(req, 'vault:get_instructions', id, 404);
        return res.status(404).json({ error: 'Token not found' });
      }

      // Get instructions
      const instructionsStmt = db.prepare(`
        SELECT * FROM vault_token_instructions WHERE token_id = ?
      `);
      const instructions = instructionsStmt.get(id);

      if (!instructions) {
        logInstructionAction(req, 'vault:get_instructions', id, 404);
        return res.status(404).json({ error: 'No instructions found for this token' });
      }

      const response = {
        id: instructions.id,
        tokenId: instructions.token_id,
        instructions: instructions.instructions,
        examples: instructions.examples ? JSON.parse(instructions.examples) : [],
        autoGenerated: instructions.auto_generated === 1,
        learnedFromSkill: instructions.learned_from_skill_id,
        learnedFromAgent: instructions.learned_from_agent_id,
        learnedAt: instructions.learned_at,
        createdAt: instructions.created_at,
        updatedAt: instructions.updated_at
      };

      logInstructionAction(req, 'vault:get_instructions', id, 200);
      res.json({ success: true, data: response });
    } catch (error) {
      logInstructionAction(req, 'vault:get_instructions', req.params.id, 500, { error: error.message });
      console.error('[VaultInstructions] Error getting instructions:', error);
      res.status(500).json({ error: 'Failed to get instructions' });
    }
  });

  /**
   * POST /api/v1/vault/tokens/:id/instructions
   * Save or create instructions for a token
   * User-provided instructions (auto_generated = false)
   */
  router.post('/tokens/:id/instructions', requireAuth, (req, res) => {
    try {
      const { id } = req.params;
      const { instructions, examples } = req.body;
      const userId = resolveUserId(req);

      // Validate input
      if (!instructions || typeof instructions !== 'string' || instructions.trim().length === 0) {
        return res.status(400).json({ error: 'Instructions text is required and must be non-empty' });
      }

      if (instructions.length > 100000) {
        return res.status(400).json({ error: 'Instructions text exceeds maximum length (100KB)' });
      }

      if (examples && !Array.isArray(examples)) {
        return res.status(400).json({ error: 'Examples must be an array' });
      }

      // Validate examples format
      if (examples) {
        try {
          JSON.stringify(examples);
        } catch {
          return res.status(400).json({ error: 'Examples must be valid JSON' });
        }
      }

      // Verify token exists and belongs to user
      const tokenStmt = db.prepare(`
        SELECT * FROM access_tokens WHERE id = ? AND owner_id = ?
      `);
      const token = tokenStmt.get(id, userId);

      if (!token) {
        logInstructionAction(req, 'vault:create_instructions', id, 403);
        return res.status(403).json({ error: 'Token not found or access denied' });
      }

      const instructionId = uuidv4();
      const now = new Date().toISOString();

      // Check if instructions already exist
      const existingStmt = db.prepare(`
        SELECT id FROM vault_token_instructions WHERE token_id = ?
      `);
      const existing = existingStmt.get(id);

      if (existing) {
        logInstructionAction(req, 'vault:create_instructions', id, 409);
        return res.status(409).json({ 
          error: 'Instructions already exist for this token. Use PUT to update.' 
        });
      }

      // Create instructions
      const insertStmt = db.prepare(`
        INSERT INTO vault_token_instructions (
          id, token_id, instructions, examples, auto_generated,
          created_at, updated_at, created_by_user_id
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?)
      `);

      insertStmt.run(
        instructionId,
        id,
        instructions,
        examples ? JSON.stringify(examples) : null,
        0, // user-provided, not auto-generated
        now,
        now,
        userId
      );

      logInstructionAction(req, 'vault:create_instructions', id, 201);

      res.status(201).json({
        success: true,
        data: {
          id: instructionId,
          tokenId: id,
          instructions,
          examples: examples || [],
          autoGenerated: false,
          createdAt: now,
          updatedAt: now
        }
      });
    } catch (error) {
      logInstructionAction(req, 'vault:create_instructions', req.params.id, 500, { error: error.message });
      console.error('[VaultInstructions] Error creating instructions:', error);
      res.status(500).json({ error: 'Failed to create instructions' });
    }
  });

  /**
   * PUT /api/v1/vault/tokens/:id/instructions
   * Update existing instructions for a token
   * Creates version history entry
   */
  router.put('/tokens/:id/instructions', requireAuth, (req, res) => {
    try {
      const { id } = req.params;
      const { instructions, examples } = req.body;
      const userId = resolveUserId(req);

      // Validate input
      if (!instructions || typeof instructions !== 'string' || instructions.trim().length === 0) {
        return res.status(400).json({ error: 'Instructions text is required and must be non-empty' });
      }

      if (instructions.length > 100000) {
        return res.status(400).json({ error: 'Instructions text exceeds maximum length (100KB)' });
      }

      if (examples && !Array.isArray(examples)) {
        return res.status(400).json({ error: 'Examples must be an array' });
      }

      // Verify token exists and belongs to user
      const tokenStmt = db.prepare(`
        SELECT * FROM access_tokens WHERE id = ? AND owner_id = ?
      `);
      const token = tokenStmt.get(id, userId);

      if (!token) {
        logInstructionAction(req, 'vault:update_instructions', id, 403);
        return res.status(403).json({ error: 'Token not found or access denied' });
      }

      // Get existing instructions
      const getStmt = db.prepare(`
        SELECT * FROM vault_token_instructions WHERE token_id = ?
      `);
      const existing = getStmt.get(id);

      if (!existing) {
        logInstructionAction(req, 'vault:update_instructions', id, 404);
        return res.status(404).json({ error: 'No instructions found for this token' });
      }

      const now = new Date().toISOString();

      // Create version history entry
      const versionId = uuidv4();
      const versionStmt = db.prepare(`
        INSERT INTO vault_token_instruction_versions (
          id, token_instruction_id, instructions_previous, instructions_new,
          examples_previous, examples_new, auto_generated_previous,
          auto_generated_new, changed_by_user_id, created_at
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
      `);

      versionStmt.run(
        versionId,
        existing.id,
        existing.instructions,
        instructions,
        existing.examples,
        examples ? JSON.stringify(examples) : null,
        existing.auto_generated,
        0, // User edit removes auto_generated flag
        userId,
        now
      );

      // Update instructions
      const updateStmt = db.prepare(`
        UPDATE vault_token_instructions
        SET instructions = ?, examples = ?, auto_generated = 0, updated_at = ?
        WHERE token_id = ?
      `);

      updateStmt.run(
        instructions,
        examples ? JSON.stringify(examples) : existing.examples,
        now,
        id
      );

      logInstructionAction(req, 'vault:update_instructions', id, 200);

      res.json({
        success: true,
        data: {
          id: existing.id,
          tokenId: id,
          instructions,
          examples: examples || (existing.examples ? JSON.parse(existing.examples) : []),
          autoGenerated: false,
          updatedAt: now
        }
      });
    } catch (error) {
      logInstructionAction(req, 'vault:update_instructions', req.params.id, 500, { error: error.message });
      console.error('[VaultInstructions] Error updating instructions:', error);
      res.status(500).json({ error: 'Failed to update instructions' });
    }
  });

  /**
   * DELETE /api/v1/vault/tokens/:id/instructions
   * Clear instructions for a token
   * Soft delete - creates archive but keeps history
   */
  router.delete('/tokens/:id/instructions', requireAuth, (req, res) => {
    try {
      const { id } = req.params;
      const userId = resolveUserId(req);

      // Verify token exists and belongs to user
      const tokenStmt = db.prepare(`
        SELECT * FROM access_tokens WHERE id = ? AND owner_id = ?
      `);
      const token = tokenStmt.get(id, userId);

      if (!token) {
        logInstructionAction(req, 'vault:delete_instructions', id, 403);
        return res.status(403).json({ error: 'Token not found or access denied' });
      }

      // Get existing instructions
      const getStmt = db.prepare(`
        SELECT * FROM vault_token_instructions WHERE token_id = ?
      `);
      const existing = getStmt.get(id);

      if (!existing) {
        logInstructionAction(req, 'vault:delete_instructions', id, 404);
        return res.status(404).json({ error: 'No instructions found for this token' });
      }

      // Create version history entry for deletion
      const now = new Date().toISOString();
      const versionId = uuidv4();
      const versionStmt = db.prepare(`
        INSERT INTO vault_token_instruction_versions (
          id, token_instruction_id, instructions_previous, instructions_new,
          examples_previous, examples_new, changed_by_user_id, change_reason, created_at
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)
      `);

      versionStmt.run(
        versionId,
        existing.id,
        existing.instructions,
        '', // Empty string indicates deletion
        existing.examples,
        null,
        userId,
        'Instructions cleared by user',
        now
      );

      // Delete instructions
      const deleteStmt = db.prepare(`
        DELETE FROM vault_token_instructions WHERE token_id = ?
      `);
      deleteStmt.run(id);

      logInstructionAction(req, 'vault:delete_instructions', id, 200);

      res.json({
        success: true,
        message: 'Instructions cleared successfully'
      });
    } catch (error) {
      logInstructionAction(req, 'vault:delete_instructions', req.params.id, 500, { error: error.message });
      console.error('[VaultInstructions] Error deleting instructions:', error);
      res.status(500).json({ error: 'Failed to delete instructions' });
    }
  });

  /**
   * GET /api/v1/vault/services/:serviceName/instructions
   * Get all instructions for a service type
   * Returns both token-specific and service-level instructions
   */
  router.get('/services/:serviceName/instructions', (req, res) => {
    try {
      const { serviceName } = req.params;

      // Get service-level instructions if they exist
      const serviceStmt = db.prepare(`
        SELECT * FROM service_type_instructions WHERE service_name = ?
      `);
      const serviceInstructions = serviceStmt.get(serviceName.toLowerCase());

      // Get all token instructions for this service type
      // This requires joining with access_tokens to find tokens for the service
      const tokenInstructionsStmt = db.prepare(`
        SELECT vti.*, at.name as token_name
        FROM vault_token_instructions vti
        JOIN access_tokens at ON vti.token_id = at.id
        WHERE at.type = ? OR at.service = ?
        ORDER BY vti.created_at DESC
      `);
      const tokenInstructions = tokenInstructionsStmt.all(
        serviceName.toLowerCase(),
        serviceName.toLowerCase()
      );

      const response = {
        serviceName: serviceName.toLowerCase(),
        serviceTemplate: serviceInstructions ? {
          id: serviceInstructions.id,
          instructions: serviceInstructions.instructions,
          examples: serviceInstructions.examples ? JSON.parse(serviceInstructions.examples) : [],
          autoGenerated: serviceInstructions.auto_generated === 1,
          aggregatedFromTokenCount: serviceInstructions.aggregated_from_token_count,
          updatedAt: serviceInstructions.updated_at
        } : null,
        tokenInstructions: tokenInstructions.map(ti => ({
          id: ti.id,
          tokenId: ti.token_id,
          tokenName: ti.token_name,
          instructions: ti.instructions,
          examples: ti.examples ? JSON.parse(ti.examples) : [],
          autoGenerated: ti.auto_generated === 1,
          createdAt: ti.created_at,
          updatedAt: ti.updated_at
        })),
        total: tokenInstructions.length
      };

      logInstructionAction(req, 'vault:get_service_instructions', serviceName, 200);
      res.json({ success: true, data: response });
    } catch (error) {
      logInstructionAction(req, 'vault:get_service_instructions', req.params.serviceName, 500, { error: error.message });
      console.error('[VaultInstructions] Error getting service instructions:', error);
      res.status(500).json({ error: 'Failed to get service instructions' });
    }
  });

  /**
   * POST /api/v1/vault/tokens/:id/learn-from-api
   * Auto-save instructions based on API interaction
   * Called by AI agents after successful API calls
   */
  router.post('/tokens/:id/learn-from-api', (req, res) => {
    try {
      const { id } = req.params;
      const { instructions, examples, errors, rateLimit, skillId, agentId } = req.body;

      // Minimal validation
      if (!instructions || typeof instructions !== 'string') {
        return res.status(400).json({ error: 'instructions must be a non-empty string' });
      }

      // Verify token exists
      const tokenStmt = db.prepare('SELECT * FROM access_tokens WHERE id = ?');
      const token = tokenStmt.get(id);

      if (!token) {
        logInstructionAction(req, 'vault:learn_from_api', id, 404);
        return res.status(404).json({ error: 'Token not found' });
      }

      const instructionId = uuidv4();
      const now = new Date().toISOString();

      // Check if instructions already exist
      const existingStmt = db.prepare(`
        SELECT id FROM vault_token_instructions WHERE token_id = ?
      `);
      const existing = existingStmt.get(id);

      if (existing) {
        // Update with learned information
        const updateStmt = db.prepare(`
          UPDATE vault_token_instructions
          SET examples = ?, learned_from_skill_id = ?, learned_from_agent_id = ?,
              learned_at = ?, updated_at = ?
          WHERE token_id = ?
        `);

        updateStmt.run(
          examples ? JSON.stringify(examples) : null,
          skillId || null,
          agentId || null,
          now,
          now,
          id
        );

        logInstructionAction(req, 'vault:learn_from_api', id, 200);
        return res.json({
          success: true,
          message: 'Instructions updated with learning',
          data: { id: existing.id, tokenId: id }
        });
      }

      // Create new instructions with auto_generated flag
      const insertStmt = db.prepare(`
        INSERT INTO vault_token_instructions (
          id, token_id, instructions, examples, auto_generated,
          learned_from_skill_id, learned_from_agent_id, learned_at,
          created_at, updated_at
        ) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
      `);

      insertStmt.run(
        instructionId,
        id,
        instructions,
        examples ? JSON.stringify(examples) : null,
        1, // auto_generated = true
        skillId || null,
        agentId || null,
        now,
        now,
        now
      );

      logInstructionAction(req, 'vault:learn_from_api', id, 201);

      res.status(201).json({
        success: true,
        message: 'Instructions learned and saved',
        data: {
          id: instructionId,
          tokenId: id,
          autoGenerated: true,
          learnedAt: now
        }
      });
    } catch (error) {
      logInstructionAction(req, 'vault:learn_from_api', req.params.id, 500, { error: error.message });
      console.error('[VaultInstructions] Error learning from API:', error);
      res.status(500).json({ error: 'Failed to save learning' });
    }
  });

  return router;
}

module.exports = createVaultInstructionsRoutes;
